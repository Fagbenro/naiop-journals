              <h4>
               Determination of Membership
              </h4>
              <ul type="I" style="text-align:justify;">
                <li><b>Honoured Fellows:</b> Honourary Fellows shall be persons of scientific distinction who have
                 generously contributed to the advancement of the division in particular and discipline of psychology in general. The total number of Honourary Fellows at any one time shall not
exceed ten (10).</li>
                 <li><b>Professional (Academic/Practitioner) Fellows:</b> To qualify for election as a Professional
                  Fellow, a person shall be a full member of the Nigerian Psychological Association and
                   the NAIOP or shall have satisfied the Executive of the division that he possesses the requirement for full membership and in either case he shall have satisfied the Executives:

                   <ul type="a">
                    <li>That he/she has been engaged in work of an industrial organisational and
                    psychological nature (other than undergraduate training) for a total period of at least Ten (10) years; and</li>
                    <li>That he/she possesses an advanced knowledge of industrial and organisational psychology; and </li>
                    <li>That he/she has made a sustainable and original contribution to the advancement of industrial and organisational psychological knowledge or practice either by his/her own research or by organising and developing the work of others.</li>
                   </ul>
                 </li>
                 <li><b>Full Members:</b> A full Member shall be:
                  <ol type="a">
                    <li>Practitioner with at least a Master degree or its equivalent in industrial and organisational psychology from any recognised University with minimum of 15 (fifteen) years work experience in relevant institutional context.</li>
                    <li>Academic with at least a PhD degree or its equivalent in industrial and organisational psychology from any recognised University with minimum of 3 (three) years work experience in relevant institutional context.</li>
                 
                  </ol>
                 </li>
                 <li><b>Associate Members:</b> An Associate Member shall be:

                  <ol type="a">
                    <li>Holder of a fresh PhD degree in Industrial and Organisational Psychology.</li>
                    <li>Master degree in Industrial and Organisational Psychology with at least 5 (five) years experience in relevant institutional contexts, (e.g. industry and educational establishments).</li>
                  </ol>
                  </li>

                  <li><b>Graduate Members:</b> A Graduate Member shall be a holder of a Master degree in
                      Industrial and Organisational Psychology and/or under years of relevant work experience
                      in Industrial and Organisational Psychology related fields.</li>
                      <li><b>Affiliate Members:</b> Affiliate Member shall be a member of other related professions
                      (e.g. who demonstrate a keen interest in psychology like Human Resource Management,
                      Chartered Institute of Personnel Management, Managerial Psychology, Security
                      Psychology etc.).</li>
                      <li><b>Student Members:</b> Student member shall be a holder of first degree in psychology and
                      enrolled in a postgraduate degree in the field of industrial and organisational psychology
                      in recognised universities for purposes of obtaining Academic Masters Degree in
                      industrial and organisational psychology.
                  </li>
              </ul>
     
        
