        The Nigerian Association of Industrial and Organisational Psychologists (NAIOP) was formally established during the Annual Scientific Conference and AGM of the Nigeria Psychological Association held at Precious Palm Royal Hotels, Benin City, Nigeria on Wednesday, 25th November, 2015. Since then the NAIOP has continued to build on its main purpose – enhancing employee wellbeing and performance. In that conference alone, the division established different platforms (see below) to ensure that her members were always in communication and could easily get information from the leadership of the division.
