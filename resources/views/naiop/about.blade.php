             <p class="mb-5">
       In 2016, during its second Annual Scientific Conference and AGM of the Nigeria Psychological
Association held at the Auditorium of the Nnamdi Azikiwe University, Awka, the I/O Division
held its first election. Professor Benjamin Osayawe Ehigie was elected as President of the
Division, while Dr. Anthony Nnedum was elected as Secretary. Other officers include Dr.
Richards Ebeh (Publicity Secretary) and Dr. Aondoaver Ucho (Financial Secretary).
        </p>

        
