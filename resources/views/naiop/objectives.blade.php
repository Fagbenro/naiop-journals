        <h1>
            AIMS AND OBJECTIVES
        </h1>
    
        <div class="col-12 mt-4">
     
          <div class="tab-content">
            <div class="tab-pane fade show active" id="tabDescription" role="tabpanel">
                    <div class="row mt-5">
              
          
                
                <div class="col-md-12 my-2" style="text-align:justify;">
                 <p>
                  The overall aim of the I/O Division of the NPA is to create a platform for enhancement of professionalism among I/O psychologists and practitioners in Nigeria and across Africa.
                 </p>
                 <p>The specific objectives of the division are as follows:</p>
                
        
                    <p>1. To acknowledge and promote the advancement, objectives and vision of the Nigerian Psychological Association wherein the division shall remain a subject, as a condition of it’s existence.</p>
                    <p>2. To promote the advancement and diffusion of a knowledge of industrial and
                          organisational psychology, pure and applied and especially to promote the efficiency and
                          usefulness of its members by setting up a high standard of professional education and
                          practice and by compelling the observance of strict rules of professional conduct as a
                          condition of membership.</p>

                                        <p>3. To establish, maintain and extend a library of books, works, or manuscripts on the
                          industrial and organisational psychological science, or the applications thereof, or other
                          subjects allied thereto, and to make provision for laboratories for research in industrial
                          and organisational psychology.</p>

                          <p>4. To make grants of money, books, apparatus, or other matters or things for the purpose of
                          promoting invention and research in industrial and organisational psychological science,
                          or its applications, or in subjects connected therewith.</p>

                                    <p>5. To arrange, provide for, or join in arranging and providing for the holding of conferences,
                          workshops, national or international exhibitions, meetings, lectures, classes, and
                          discussions on subjects of general and special interest to persons interested in industrial
                          and organisational psychology, and for the exhibitions of any new, improved, or other
                          apparatus for industrial and organisational psychological research, and any new or other
                          experiments illustrative of psychological laws or phenomena calculated directly or
                          indirectly to further the objectives of the division, and to appoint and provide the
                          reasonable expenses of representatives of this division attending such conferences,
                          national or international exhibitions, meetings, lectures, classes or discussions.</p>

                                <p>6. To prepare, edit, print, publish, issue and circulate, free of charge, and or to sell, lend,
                          issue and distribute free of charge communications made to the division, or document
                          relating to psychology, and any report of the proceedings and accounts of the division.</p>

                          <p>7. To keep a register of members, their qualifications, appointments and career
                          progressions.</p>
                                <p>8. To purchase, take on lease, or in exchange, hire, or otherwise acquired in any manner and
                          hold any real or personal property, and any rights or privileges which the division may
                          think necessary or convenient for its purposes.</p>

                          <p>9. To construct, alter, pull down, decorate, maintain, furnish, fit up, and improve any
                          buildings and undertake and execute any works necessary or convenient for the purposes
                          of the division.</p>

                          <p>10. To obtain, collect, and receive money and funds by way of contributions, donations,
                          subscriptions, legacies, grants or any other lawful methods, and (subject to the provision
                          of the said section) to accept and receive gifts of property of any special trusts or not for
                          or towards the objects of the division, and to administer such funds and property.</p>

                          <p>11. To borrow or raise money in such manner and upon terms as the division shall think fit,
                          and in particular upon the security by way of mortgage, charge, debenture or otherwise of
                          all or any part of the property of the division.</p>

                          <p>12. To make, draw, endorse, execute and issue promissory notes, bills of exchange,
                          debentures, and other negotiable or transferable instruments.</p>

                          <p>13. To sell, improve, manage, develop, exchange, let, mortgage, dispose of, turn to account
                          or otherwise deal with all or any part of the property rights, and privileges of the division
                          as may be deemed expedient, with a view to the promotion of its objectives.</p>

                          <p>14. To enter into any arrangements with any authorities, Nigerian Psychological Association,
                          Federal, State, Municipal, Local Government or otherwise, which may seem conducive to
                          the Division’s memoranda, and to obtain from any such authorities rights, professional
                          and concessions which it may seem desirable to obtain, and to carry out and comply with
                          any such arrangements, rights, privileges and concessions.</p>

                          <p>15. To apply for, promote and obtain, or join in applying for, promoting, or obtaining any
                          Act of the National Assembly, Provisional Order, or any authority, necessary or desirable
                          for the furtherance or realisation of any of the objects of the Nigerian Psychological
                          Association and the division, and to take all such steps and proceedings, and to do all
                          such acts and things, either alone or jointly with the Nigerian Psychological Association.
                          Whether by opposing applications or proceedings or otherwise as may seem necessary or
                          expedient to protect or further the interests of the division in line with the constitution of
                          the Nigerian Psychological Association.</p>

                          <p>16. To federate or amalgamate with, affiliate or become affiliated to any interest body not
                          formed for profit or gain and having similar objectives, wholly established for teaching or
                          encouraging industrial and organisational psychology and to acquire and undertake all or
                          any part of the assets, liabilities and engagements of any of such body.</p>

                          <p>17. Subject to the provisions hereinafter contained, to employ and pay superintendents,
                          managers, secretaries, accountants, auditors, clerks and any other persons or bodies
                          whose services are required or deemed expedient for carrying out any of the objectives of
                          the division.</p>
                          <p>18. To establish a benevolent and or to provide for the welfare of members, employees or exemployees of the division, and families of such persons by grants of money, pensions or
                          otherwise as the division may determine from time to time.</p>

                          <p>19. To undertake, accept any trusts or obligations which may seem in accordance with the
                          objects of the division, and to perform any services in connection with the objects of the
                          division.</p>

                          <p>20. To do all or any of the above things as principals, agents, trustees or otherwise, and by or
                          through trustees, agents or otherwise, and either alone or in conjunction with the Nigerian
                          Psychological Association.</p>

                          <p>21. To pay all expenses, preliminary or incidental, to the incorporation of the division and its
                          registration.</p>

                          <p>22. To do all such other things as may be incidental or conducive to the attainment of the
                          above objectives.</p>

                          <p>23. Provided that the division shall not support with its funds any objective or endorsed by its
                          members or others any regulation, restriction or condition which, if an objective of the
                          Division, would make it a Trade Union.</p>

                   
