        <h3> Note To Contributors</h3>
     
        <p class="mb-5">
Journal of Industrial and Organisational Behaviour in Africa is an annual publication of the Nigerian Association of Industrial and Organizational Psychologists  (NAIOP) which owns the copy right. NAIO is a division of the Nigerian Psychological Association (NPA). The journal is open to well researched scholarly articles aimed at enhancing employee wellbeing and performance in Nigeria and the African continent.
        </p>

<h3>Editorial Policy</h3>
            <p>JIOBA  has the following objectives.</p>

<p>i.  Development of a research culture among Industrial â€“ Organisational psychologists.</p>
<p>ii. Publication of peer review articles that meet the aim of enhancing employee wellbeing in an ever changing work environment. </p>
<p>iii.  Formulation of research based policies for improvement of human factor issues across organisations and institutions in Nigeria and Africa.</p>
<p>iv. Capturing employee behaviour dynamics in the workplace that have implication for development of new psychological theories and techniques.</p>


      <h3>Submission of Manuscripts</h3>

<p class="mb-5">Manuscripts are to be submitted electronically in Microsoft Windows. Papers should be double-line spaced in Times New Roman format with font size 12. Each paper must not exceed 25 pages including references.</p>

<h3>In-text citations and References  </h3>
<p class="mb-5">In-text citations must be listed correctly in the References and should be APA (NPA) format compliant.</p>

<h3>Assessment</h3>


<p class="mb-5">All manuscripts received by the editorial committee shall be sent for independent assessment/peer review. Results of such assessments will be communicated to authors as appropriate. The editorial committee of JIOBA will not accept any paper that had already been published or sent for publication in any other journal. An undertaking to this effect shall be required from authors. </p>

<h3>Corresponding Authors</h3>

<p class="mb-5">Manuscripts with multiple authors shall indicate a corresponding author with whom the editorial team will communicate.</p>


